#include <stdio.h>
#include <unistd.h>

int	ft_strlen(char *str)
{
	int	i;

	i = 0;
	while (*str != '\0')
	{
	i++;
	*str++;
	}
	return i;
}

int	main(void)
{
	char str[] = "This is a string";
	char* pstr = str;
	ft_strlen(pstr);
	return (0);
}
