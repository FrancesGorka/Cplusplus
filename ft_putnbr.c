#include <unistd.h>
#include <stdio.h>

void	ft_putnbr(int nb)
{
	char	cnb;

	if (nb < 0)
		write(1, "-", 1);
	if (nb >= 10)
	{
		ft_putnbr(nb / 10);
		ft_putnbr(nb % 10);
	}
	else
	{
		cnb = 48 + nb;
		write(1, &cnb, 1);
	}
}
